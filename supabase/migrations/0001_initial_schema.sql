create index idx_inventory_category    on public.inventory_items(category_id);
create index idx_inventory_location    on public.inventory_items(location_id);
create unique index uq_inventory_user_barcode on public.inventory_items(user_id, barcode) where barcode is not null;

alter table public.inventory_items enable row level security;
