-- ============================================================================
-- Initial schema migration for the web/Supabase port of Inventory Tracking App.
-- Translated from the SQLite schema in src/app/services/database.service.ts
-- (the offline Capacitor version).
--
-- Key changes vs SQLite:
--   * Drops `users` and `sessions` tables - replaced by Supabase Auth (auth.users)
--   * Every per-user table uses uuid user_id referencing auth.users
--   * boolean instead of INTEGER 0/1
--   * timestamptz instead of TEXT/DATETIME
--   * Adds missing indexes flagged in the security audit
--   * Adds UNIQUE(user_id, plan_date, meal_type) to fix the duplicate-slot
--     bug found in the uncommitted meal-plan diff
--   * RLS enabled on every per-user table with owner-only policies
-- ============================================================================

create extension if not exists "pgcrypto";

-- ----------------------------------------------------------------------------
-- categories  (global read-only seed)
-- ----------------------------------------------------------------------------
create table public.categories (
  id      bigint generated by default as identity primary key,
  name    text not null unique,
  icon    text,
  color   text
);

alter table public.categories enable row level security;

-- Anyone (authenticated) can read categories. No write policies = no client writes.
create policy "categories_read_authenticated"
  on public.categories for select
  to authenticated
  using (true);

-- ----------------------------------------------------------------------------
-- locations  (per user)
-- ----------------------------------------------------------------------------
create table public.locations (
  id            bigint generated by default as identity primary key,
  user_id       uuid not null references auth.users(id) on delete cascade,
  name          text not null,
  sub_location  text,
  created_at    timestamptz not null default now()
);

create index idx_locations_user on public.locations(user_id);

alter table public.locations enable row level security;
create policy "locations_owner_select" on public.locations for select using (auth.uid() = user_id);
create policy "locations_owner_insert" on public.locations for insert with check (auth.uid() = user_id);
create policy "locations_owner_update" on public.locations for update using (auth.uid() = user_id) with check (auth.uid() = user_id);
create policy "locations_owner_delete" on public.locations for delete using (auth.uid() = user_id);

-- ----------------------------------------------------------------------------
-- inventory_items
-- ----------------------------------------------------------------------------
create table public.inventory_items (
  id                       bigint generated by default as identity primary key,
  user_id                  uuid not null references auth.users(id) on delete cascade,
